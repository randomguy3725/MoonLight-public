package wtf.moonlight.gui.altmanager.login.exception;

public class AltLoginException extends Exception {

    public AltLoginException() {
    }

    public AltLoginException(String message) {
        super(message);
    }

    public AltLoginException(String message, Throwable cause) {
        super(message, cause);
    }

    public AltLoginException(Throwable cause) {
        super(cause);
    }

    public AltLoginException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
